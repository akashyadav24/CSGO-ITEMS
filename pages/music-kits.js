import Head from "next/head";
import { useEffect, useState } from "react";
import ItemCard from "../components/ItemCard";
import SpinnerLoader from "../components/SpinnerLoader";
import { getMusicKits } from "../services/csgo";

export default function MusicKits() {
  const [loading, setLoading] = useState(false);
  const [musicKits, setMusicKits] = useState([]);
  const [showedItems, setShowedItems] = useState([]);

  useEffect(() => {
    async function getData() {
      try {
        setLoading(true);

        const { data } = await getMusicKits();

        setShowedItems(data.splice(0, 20));
        setMusicKits(data);
        setLoading(false);
      } catch (e) {
        setMusicKits([]);
        console.error(e);
      }
    }
    getData();
  }, []);

  useEffect(() => {
    window.addEventListener("scroll", handleScroll);
    return () => {
      window.removeEventListener("scroll", handleScroll);
    };
  });

  const handleScroll = () => {
    const lastItemLoaded = document.querySelector(
      ".items-grid > .group:last-child"
    );
    if (lastItemLoaded) {
      const lastItemLoadedOffset =
        lastItemLoaded.offsetTop + lastItemLoaded.clientHeight;
      const pageOffset = window.pageYOffset + window.innerHeight;

      if (pageOffset > lastItemLoadedOffset) {
        if (musicKits.length) {
          const all = musicKits;
          const selected = all.splice(0, 20);
          setShowedItems([...showedItems, ...selected]);
          setMusicKits(all);
        }
      }
    }
  };

  return (
    <>
      <Head>
        <title>Music kits - CSGO ITEMS</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <SpinnerLoader loading={loading} />
      <div className="items-grid">
        {showedItems.map((item) => {
          return (
            <ItemCard
              key={item.id}
              route={{ type: "music-kits", id: item.id }}
              name={item.name}
              rarity={item.rarity}
              image={item.image}
            ></ItemCard>
          );
        })}
      </div>
    </>
  );
}
