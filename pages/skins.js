import Head from "next/head";
import { useEffect, useState } from "react";
import ItemCard from "../components/ItemCard";
import SpinnerLoader from "../components/SpinnerLoader";
import { getSkins } from "../services/csgo";

export default function Skins() {
  const [loading, setLoading] = useState(false);
  const [skins, setSkins] = useState([]);
  const [showedItems, setShowedItems] = useState([]);

  useEffect(() => {
    async function getData() {
      try {
        setLoading(true);

        const { data } = await getSkins();

        setShowedItems(data.splice(0, 20));
        setSkins(data);
        setLoading(false);
      } catch (e) {
        setSkins([]);
        console.error(e);
      }
    }
    getData();
  }, []);

  useEffect(() => {
    window.addEventListener("scroll", handleScroll);
    return () => {
      window.removeEventListener("scroll", handleScroll);
    };
  });

  const handleScroll = () => {
    const lastUserLoaded = document.querySelector(
      ".items-grid > .group:last-child"
    );
    if (lastUserLoaded) {
      const lastUserLoadedOffset =
        lastUserLoaded.offsetTop + lastUserLoaded.clientHeight;
      const pageOffset = window.pageYOffset + window.innerHeight;

      if (pageOffset > lastUserLoadedOffset) {
        if (skins.length) {
          const all = skins;
          const selected = all.splice(0, 20);
          setShowedItems([...showedItems, ...selected]);
          setSkins(all);
        }
      }
    }
  };

  return (
    <>
      <Head>
        <title>Skins - CSGO DATA</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <SpinnerLoader loading={loading} />
      <div className="items-grid">
        {showedItems.map((item) => {
          return (
            <ItemCard
              key={item.id}
              name={item.name}
              image={item.image}
              rarity={item.rarity}
            ></ItemCard>
          );
        })}
      </div>
    </>
  );
}
